#include <stdio.h>
#include <stdlib.h>

// -----------------------------------------------------------------------------
// Exemplo 1
// -----------------------------------------------------------------------------

typedef int Item;
typedef struct no {
   Item item;
   struct no *prox;
} *Lista;


// -----------------------------------------------------------------------------
// Exemplo 2
// -----------------------------------------------------------------------------

Lista no(Item x, Lista p) {
   Lista n = malloc(sizeof(struct no));
   n->item = x;
   n->prox = p;
   return n;
}


// -----------------------------------------------------------------------------
// Exemplo 4
// -----------------------------------------------------------------------------

void ins(Item x, Lista *L) {
   while( *L != NULL && (*L)->item < x ) 
      L = &(*L)->prox;
   *L = no(x,*L);
}




// -----------------------------------------------------------------------------
// Exemplo 5
// -----------------------------------------------------------------------------

void rem(Item x, Lista *L) {
   while( *L != NULL && (*L)->item < x ) 
      L = &(*L)->prox;
   if( *L == NULL || (*L)->item > x ) return;
   Lista n = *L;
   *L = n->prox;
   free(n);
}


// -----------------------------------------------------------------------------
// Exemplo 6
// -----------------------------------------------------------------------------

int pert(Item x, Lista L) {
   while( L != NULL && L->item < x ) 
      L = L->prox;
   return (L != NULL && L->item == x);
}

// EXIBE-----------------------------------------------------------------------------

void exibe(Lista L) {
    printf("[");
    while(L) {
      printf("%d",L->item);
      if(L->prox) printf(",");
      L = L->prox;
   }
   printf("]\n");
}